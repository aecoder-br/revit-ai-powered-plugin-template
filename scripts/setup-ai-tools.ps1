param(
  [string[]]$Tools = @('All'),
  [ValidateSet('Copy','Symlink')]
  [string]$Mode = 'Copy',
  [switch]$Force,
  [switch]$Validate,
  [switch]$ValidateAdapters,
  [string]$RootPath
)

$ErrorActionPreference = 'Stop'

if ([string]::IsNullOrWhiteSpace($RootPath)) {
  $RootPath = Split-Path -Parent (Split-Path -Parent $MyInvocation.MyCommand.Path)
}

$root = (Resolve-Path -LiteralPath $RootPath).Path
$sourceSkills = Join-Path $root '.agents\skills'
$generatedMarker = 'Generated by scripts/setup-ai-tools.ps1. Source: .agents/skills.'
$validTools = @('All','Codex','Claude','Cursor','Copilot','None')

if (-not (Test-Path -LiteralPath $sourceSkills)) {
  throw "Canonical skills directory not found: $sourceSkills"
}

function Get-SelectedTools {
  param([string[]]$RequestedTools)

  $normalizedTools = @(
    foreach ($tool in $RequestedTools) {
      foreach ($part in ($tool -split ',')) {
        $trimmed = $part.Trim()
        if (-not [string]::IsNullOrWhiteSpace($trimmed)) {
          $trimmed
        }
      }
    }
  )

  foreach ($tool in $normalizedTools) {
    if ($validTools -notcontains $tool) {
      throw "Unsupported tool '$tool'. Valid tools: $($validTools -join ', ')."
    }
  }

  if (($normalizedTools -contains 'None') -and ($normalizedTools.Count -gt 1)) {
    throw "-Tools None cannot be combined with other tools."
  }

  if ($normalizedTools -contains 'None') {
    return @()
  }

  if ($normalizedTools -contains 'All') {
    return @('Codex','Claude','Cursor','Copilot')
  }

  return @($normalizedTools | Select-Object -Unique)
}

function Test-FileContains {
  param(
    [string]$Path,
    [string]$Needle
  )

  if (-not (Test-Path -LiteralPath $Path)) {
    return $false
  }

  $text = Get-Content -Raw -LiteralPath $Path
  return $text.Contains($Needle)
}

function Set-GeneratedFile {
  param(
    [string]$Path,
    [string]$Content,
    [string]$Description
  )

  if (Test-Path -LiteralPath $Path) {
    if ($Force) {
      Set-Content -LiteralPath $Path -Value $Content -Encoding UTF8
      Write-Host "  Replaced $Description because -Force was specified: $Path" -ForegroundColor Yellow
      return
    }

    Write-Host "  Validating existing ${Description}: $Path" -ForegroundColor Cyan
    if (-not (Test-FileContains -Path $Path -Needle $generatedMarker)) {
      Write-Warning "Existing $Description does not contain the setup generation marker. It was not overwritten. Use -Force only if replacing it is intentional."
    }
    return
  }

  $parent = Split-Path -Parent $Path
  if (-not [string]::IsNullOrWhiteSpace($parent)) {
    New-Item -ItemType Directory -Force -Path $parent | Out-Null
  }

  Set-Content -LiteralPath $Path -Value $Content -Encoding UTF8
  Write-Host "  Created ${Description}: $Path" -ForegroundColor Green
}

function Copy-SkillMirror {
  param(
    [string]$ToolName,
    [string]$DestinationRoot
  )

  Write-Host "Refreshing $ToolName skill mirror from .agents/skills: $DestinationRoot" -ForegroundColor Cyan
  New-Item -ItemType Directory -Force -Path $DestinationRoot | Out-Null

  $sourceDirectories = @(Get-ChildItem -LiteralPath $sourceSkills -Directory -Force)
  foreach ($sourceDirectory in $sourceDirectories) {
    $targetDirectory = Join-Path $DestinationRoot $sourceDirectory.Name

    if (Test-Path -LiteralPath $targetDirectory) {
      if (-not $Force) {
        Write-Warning "Skipping existing skill mirror '$targetDirectory'. Use -Force to replace it."
        continue
      }

      $resolvedTarget = (Resolve-Path -LiteralPath $targetDirectory).Path
      $resolvedDestinationRoot = (Resolve-Path -LiteralPath $DestinationRoot).Path
      if (-not $resolvedTarget.StartsWith($resolvedDestinationRoot, [System.StringComparison]::OrdinalIgnoreCase)) {
        throw "Refusing to remove path outside mirror root: $resolvedTarget"
      }

      Remove-Item -LiteralPath $targetDirectory -Recurse -Force
    }

    if ($Mode -eq 'Symlink') {
      New-Item -ItemType SymbolicLink -Path $targetDirectory -Target $sourceDirectory.FullName | Out-Null
      Write-Host "  Linked $($sourceDirectory.Name)" -ForegroundColor Green
    } else {
      Copy-Item -LiteralPath $sourceDirectory.FullName -Destination $targetDirectory -Recurse -Force
      Write-Host "  Copied $($sourceDirectory.Name)" -ForegroundColor Green
    }
  }
}

