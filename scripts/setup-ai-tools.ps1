  param(
    [string]$Name,
    [string]$Description,
    [string]$Skill,
    [string]$Workflow
  )

  return @"
---
name: $Name
description: $Description
agent: agent
---

<!-- $generatedMarker -->

Use AGENTS.md and $Workflow as canonical guidance.

Use .agents/skills/$Skill/SKILL.md for the specialized role instructions.

Do not duplicate long skill content in this prompt.
"@
}

function Ensure-CopilotAdapter {
  Write-Host 'Validating GitHub Copilot static adapter.' -ForegroundColor Cyan
  Write-Host '  Copilot uses .github/copilot-instructions.md and .github/prompts; no skill mirror is generated.' -ForegroundColor Cyan

  $githubRoot = Join-Path $root '.github'
  $promptRoot = Join-Path $githubRoot 'prompts'
  New-Item -ItemType Directory -Force -Path $promptRoot | Out-Null

  Set-GeneratedFile `
    -Path (Join-Path $githubRoot 'copilot-instructions.md') `
    -Content (Get-CopilotInstructionsContent) `
    -Description 'Copilot instructions'

  $prompts = @(
    @{ Name = 'feature-plan'; Description = 'Plan a Revit AI template feature without implementing code.'; Skill = 'orchestrator-feature-lead'; Workflow = '.agents/workflows/feature-lifecycle.md' },
    @{ Name = 'product-requirements'; Description = 'Translate a product request into actionable Revit plugin requirements.'; Skill = 'requirements-analyst'; Workflow = '.agents/workflows/feature-lifecycle.md' },
    @{ Name = 'revit-api-review'; Description = 'Review Revit API usage, threading, ExternalEvent routing, and transactions.'; Skill = 'revit-api-senior'; Workflow = '.agents/workflows/validation-gates.md' },
    @{ Name = 'multiversion-impact'; Description = 'Review Revit 2024-2027 framework and API compatibility impact.'; Skill = 'revit-multiversion-architect'; Workflow = '.agents/workflows/validation-gates.md' },
    @{ Name = 'security-review'; Description = 'Review AI, MCP, secrets, privacy, and Revit write-operation security.'; Skill = 'cybersecurity-privacy-engineer'; Workflow = '.agents/workflows/validation-gates.md' },
    @{ Name = 'qa-validation'; Description = 'Create or run validation for Revit AI template changes.'; Skill = 'qa-automation-engineer'; Workflow = '.agents/workflows/validation-gates.md' },
    @{ Name = 'pr-summary'; Description = 'Prepare a concise PR summary grounded in the final diff and validation.'; Skill = 'pr-reviewer'; Workflow = '.agents/workflows/handoff-contract.md' },
    @{ Name = 'template-setup'; Description = 'Guide setup of AI tooling and template creation choices.'; Skill = 'visual-studio-template-engineer'; Workflow = '.agents/workflows/feature-lifecycle.md' }
  )

  foreach ($prompt in $prompts) {
    $promptPath = Join-Path $promptRoot "$($prompt.Name).prompt.md"
    $content = Get-CopilotPromptContent `
      -Name $prompt.Name `
      -Description $prompt.Description `
      -Skill $prompt.Skill `
      -Workflow $prompt.Workflow

    Set-GeneratedFile -Path $promptPath -Content $content -Description "Copilot prompt $($prompt.Name)"
  }
}

function Invoke-ScriptChecked {
  param(
    [scriptblock]$Command,
    [string]$Description
  )

  & $Command
  if ($LASTEXITCODE -ne 0) {
    throw "$Description failed with exit code $LASTEXITCODE."
  }
}

$selectedTools = Get-SelectedTools -RequestedTools $Tools

foreach ($tool in $selectedTools) {
  switch ($tool) {
    'Codex' {
      Ensure-CodexAdapter
    }
    'Claude' {
      Copy-SkillMirror -ToolName 'Claude' -DestinationRoot (Join-Path $root '.claude\skills')
    }
    'Cursor' {
      Copy-SkillMirror -ToolName 'Cursor' -DestinationRoot (Join-Path $root '.cursor\skills')
    }
    'Copilot' {
      Ensure-CopilotAdapter
    }
    default {
      throw "Unsupported tool: $tool"
    }
  }
}

if ($Validate) {
  $skillValidator = Join-Path $root 'scripts\validate-skills.ps1'
  if (-not (Test-Path -LiteralPath $skillValidator)) {
    throw "Skill validation script not found: $skillValidator"
  }

  Invoke-ScriptChecked `
    -Command { & $skillValidator -RootPath $root -IncludeMirrors } `
    -Description 'Skill validation'

  $ValidateAdapters = $true
}

if ($ValidateAdapters) {
  $adapterTools = $selectedTools
  if ($adapterTools.Count -eq 0) {
    $adapterTools = @('All')
  }

  $adapterValidator = Join-Path $root 'scripts\validate-ai-adapters.ps1'
  if (-not (Test-Path -LiteralPath $adapterValidator)) {
    throw "AI adapter parity validation script not found: $adapterValidator"
  }

  Invoke-ScriptChecked `
    -Command { & $adapterValidator -RootPath $root -Tools $adapterTools } `
    -Description 'AI adapter parity validation'
}

Write-Host 'AI tool setup completed.' -ForegroundColor Green
