function Ensure-CodexAdapter {
  Write-Host 'Validating Codex static adapter.' -ForegroundColor Cyan
  Write-Host '  Codex reads .agents/skills directly; no Codex skill mirror is generated.' -ForegroundColor Cyan

  $codexRoot = Join-Path $root '.codex'
  New-Item -ItemType Directory -Force -Path $codexRoot | Out-Null

  Set-GeneratedFile `
    -Path (Join-Path $codexRoot 'README.md') `
    -Content (Get-CodexReadmeContent) `
    -Description 'Codex README'

  Set-GeneratedFile `
    -Path (Join-Path $codexRoot 'config.toml') `
    -Content (Get-CodexConfigContent) `
    -Description 'Codex config'
}

